public class Device {
    private String delay_value, line, icon, device_name_tv;

    public String getDelay_value() {
        return delay_value;
    }
    public void setDelay_value(String delay_value) {
        this.delay_value = delay_value;
    }

    public String getLine() {
        return line;
    }
    public void setLine(String line) {
        this.line = line;
    }

    public String getIcon() {
        return icon;
    }
    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getDevice_name_tv() {
        return device_name_tv;
    }
    public void setDevice_name_tv(String device_name_tv) {
        this.device_name_tv = device_name_tv;
    }

}
